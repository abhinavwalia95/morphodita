static const int english_tokenizer_split_token_start = 1;



unsigned english_tokenizer::split_token(const char* begin, const char* end) {
  const char* p = begin; int cs;
  unsigned split_len = 0, split_mark = 0;
  
	{
	cs = english_tokenizer_split_token_start;
	}

	{
	int _klen;
	const char *_keys;
	int _trans;

	if ( p == ( end) )
		goto _test_eof;
	if ( cs == 0 )
		goto _out;
_resume:
	_keys = _english_tokenizer_split_token_trans_keys + _english_tokenizer_split_token_key_offsets[cs];
	_trans = _english_tokenizer_split_token_index_offsets[cs];

	_klen = _english_tokenizer_split_token_single_lengths[cs];
	if ( _klen > 0 ) {
		const char *_lower = _keys;
		const char *_mid;
		const char *_upper = _keys + _klen - 1;
		while (1) {
			if ( _upper < _lower )
				break;

			_mid = _lower + ((_upper-_lower) >> 1);
			if ( ( begin[end - p - 1]) < *_mid )
				_upper = _mid - 1;
			else if ( ( begin[end - p - 1]) > *_mid )
				_lower = _mid + 1;
			else {
				_trans += (unsigned int)(_mid - _keys);
				goto _match;
			}
		}
		_keys += _klen;
		_trans += _klen;
	}

	_klen = _english_tokenizer_split_token_range_lengths[cs];
	if ( _klen > 0 ) {
		const char *_lower = _keys;
		const char *_mid;
		const char *_upper = _keys + (_klen<<1) - 2;
		while (1) {
			if ( _upper < _lower )
				break;

			_mid = _lower + (((_upper-_lower) >> 1) & ~1);
			if ( ( begin[end - p - 1]) < _mid[0] )
				_upper = _mid - 2;
			else if ( ( begin[end - p - 1]) > _mid[1] )
				_lower = _mid + 2;
			else {
				_trans += (unsigned int)((_mid - _keys)>>1);
				goto _match;
			}
		}
		_trans += _klen;
	}

_match:
	_trans = _english_tokenizer_split_token_indicies[_trans];
	cs = _english_tokenizer_split_token_trans_targs[_trans];

	if ( _english_tokenizer_split_token_trans_actions[_trans] == 0 )
		goto _again;

	switch ( _english_tokenizer_split_token_trans_actions[_trans] ) {
	case 2:
	{ split_len = p - begin + 1; {p++; goto _out; } }
	break;
	case 1:
	{ split_mark = p - begin + 1; }
	break;
	}

_again:
	if ( cs == 0 )
		goto _out;
	if ( ++p != ( end) )
		goto _resume;
	_test_eof: {}
	if ( p == ( end) )
	{
	switch ( _english_tokenizer_split_token_eof_actions[cs] ) {
	case 3:
	{ split_len = split_mark; {p++; goto _out; } }
	break;
	}
	}

	_out: {}
	}


  return split_len == end - begin ? 0 : split_len;
}


