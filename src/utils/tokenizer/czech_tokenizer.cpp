bool czech_tokenizer::next_sentence(vector<string_piece>& forms) {
  int cs, act;
  const char* ts;
  const char* te;
  
	{
	cs = czech_tokenizer_start;
	ts = 0;
	te = 0;
	act = 0;
	}

	{
	int _klen;
	const unsigned char *_keys;
	int _trans;

	if ( ( text) == ( text_end) )
		goto _test_eof;
_resume:
	switch ( _czech_tokenizer_from_state_actions[cs] ) {
	case 5:
	{ts = ( text);}
	break;
	}

	_keys = _czech_tokenizer_trans_keys + _czech_tokenizer_key_offsets[cs];
	_trans = _czech_tokenizer_index_offsets[cs];

	_klen = _czech_tokenizer_single_lengths[cs];
	if ( _klen > 0 ) {
		const unsigned char *_lower = _keys;
		const unsigned char *_mid;
		const unsigned char *_upper = _keys + _klen - 1;
		while (1) {
			if ( _upper < _lower )
				break;

			_mid = _lower + ((_upper-_lower) >> 1);
			if ( ( (unsigned char)*text) < *_mid )
				_upper = _mid - 1;
			else if ( ( (unsigned char)*text) > *_mid )
				_lower = _mid + 1;
			else {
				_trans += (unsigned int)(_mid - _keys);
				goto _match;
			}
		}
		_keys += _klen;
		_trans += _klen;
	}

	_klen = _czech_tokenizer_range_lengths[cs];
	if ( _klen > 0 ) {
		const unsigned char *_lower = _keys;
		const unsigned char *_mid;
		const unsigned char *_upper = _keys + (_klen<<1) - 2;
		while (1) {
			if ( _upper < _lower )
				break;

			_mid = _lower + (((_upper-_lower) >> 1) & ~1);
			if ( ( (unsigned char)*text) < _mid[0] )
				_upper = _mid - 2;
			else if ( ( (unsigned char)*text) > _mid[1] )
				_lower = _mid + 2;
			else {
				_trans += (unsigned int)((_mid - _keys)>>1);
				goto _match;
			}
		}
		_trans += _klen;
	}

_match:
	_trans = _czech_tokenizer_indicies[_trans];
_eof_trans:
	cs = _czech_tokenizer_trans_targs[_trans];

	if ( _czech_tokenizer_trans_actions[_trans] == 0 )
		goto _again;

	switch ( _czech_tokenizer_trans_actions[_trans] ) {
	case 2:
	{te = ( text)+1;}
	break;
	case 6:
	{te = ( text)+1;{ forms.emplace_back(ts, te - ts); }}
	break;
	case 9:
	{te = ( text)+1;{ {( text)++; goto _out; } }}
	break;
	case 10:
	{te = ( text);( text)--;{ forms.emplace_back(ts, te - ts); }}
	break;
	case 7:
	{te = ( text);( text)--;}
	break;
	case 8:
	{te = ( text);( text)--;{ {( text)++; goto _out; } }}
	break;
	case 3:
	{{( text) = ((te))-1;}{ forms.emplace_back(ts, te - ts); }}
	break;
	case 1:
	{{( text) = ((te))-1;}}
	break;
	}

_again:
	switch ( _czech_tokenizer_to_state_actions[cs] ) {
	case 4:
	{ts = 0;}
	break;
	}

	if ( ++( text) != ( text_end) )
		goto _resume;
	_test_eof: {}
	if ( ( text) == ( text_end) )
	{
	if ( _czech_tokenizer_eof_trans[cs] > 0 ) {
		_trans = _czech_tokenizer_eof_trans[cs] - 1;
		goto _eof_trans;
	}
	}

	_out: {}
	}



  return !forms.empty();
}

